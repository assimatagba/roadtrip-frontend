<template>
    <section :class="sectionClasses">
        <b-field :label="label">
            <b-autocomplete
                rounded
                v-model="name"
                :data="filteredDataArray"
                :placeholder="placeholder"
                icon="magnify"
                clearable
                @select="option => selected = option">
                <template #empty>No results found</template>
            </b-autocomplete>
        </b-field>
    </section>
</template>

<script>
    export default {
        props: ["placeholder","sectionClasses","label"],
        methods: {
            handleChange(e){

                alert(e.target.value)

            }
        },
        data() {
            return {
                data: [
                    'Angular',
                    'Angular 2',
                    'Aurelia',
                    'Backbone',
                    'Ember',
                    'jQuery',
                    'Meteor',
                    'Node.js',
                    'Polymer',
                    'React',
                    'RxJS',
                    'Vue.js'
                ],
                name: '',
                selected: null
            }
        },
        mounted(){
        },
        computed: {
            filteredDataArray() {
                return this.data.filter((option) => {
                    return option
                        .toString()
                        .toLowerCase()
                        .indexOf(this.name.toLowerCase()) >= 0
                })
            }
        }
    }
</script>